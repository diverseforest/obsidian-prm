/**
 * 智能解析地理位置名称，输出经纬度坐标
 * 支持格式: 
 *   - "巩义" -> 直接匹配
 *   - "郑州 巩义" -> 精准前缀匹配
 *   - "甘肃省兰州市" -> 去除省、市、区、县后缀进行模糊匹配
 * @param locationName 用户输入的城市属性文本
 */
export function resolveCoordinates(locationName: string): [number, number] | null {
    if (!locationName) return null;
    const name = locationName.trim();
    
    // 1. 尝试直接精准查找
    if (COORDINATES_DB[name]) {
        const coords = COORDINATES_DB[name];
        return [coords[1], coords[0]]; // Leaflet 需要的是 [latitude, longitude]，我们数据库里存的是 [lng, lat]
    }
    
    // 2. 尝试去掉常见的“省”、“市”、“区”、“县”、“级”等后缀后匹配
    const cleanName = name.replace(/(省|市|区|县|级市)$/g, "");
    if (COORDINATES_DB[cleanName]) {
        const coords = COORDINATES_DB[cleanName];
        return [coords[1], coords[0]];
    }

    // 3. 处理带空格或层级的分隔格式（例如 "北京 朝阳", "河南 巩义")
    // 提取出最后的县区，以及前面的父级前缀
    const parts = name.split(/\s+/).map(p => p.trim());
    if (parts.length > 1) {
        const parent = parts[0].replace(/(省|市)$/g, "");
        const child = parts[parts.length - 1].replace(/(区|县|市)$/g, "");
        
        // 拼接成 "父级 子级" 格式查询
        const combo1 = `${parent} ${child}`;
        const combo2 = `${parent}${child}`;
        
        if (COORDINATES_DB[combo1]) {
            const coords = COORDINATES_DB[combo1];
            return [coords[1], coords[0]];
        }
        if (COORDINATES_DB[combo2]) {
            const coords = COORDINATES_DB[combo2];
            return [coords[1], coords[0]];
        }
        
        // 如果拼接查不到，退而直接查子级
        if (COORDINATES_DB[child]) {
            const coords = COORDINATES_DB[child];
            return [coords[1], coords[0]];
        }
    }
    
    // 4. 前缀包含型匹配（如果字典里的词是输入词的前缀，比如字典里有“卧龙区”，输入的是“南阳卧龙区”）
    for (const key of Object.keys(COORDINATES_DB)) {
        if (name.includes(key) || key.includes(cleanName)) {
            const coords = COORDINATES_DB[key];
            return [coords[1], coords[0]];
        }
    }
    
    return null;
}
